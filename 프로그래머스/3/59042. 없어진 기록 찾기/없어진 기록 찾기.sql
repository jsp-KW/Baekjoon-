# 입양 OUT 기록은 있는데, 들어온 기록이 없는 동물의 id와 이름 을 id순으로 조회
SELECT OUTS.ANIMAL_ID, OUTS.NAME FROM ANIMAL_INS INS RIGHT OUTER JOIN ANIMAL_OUTS OUTS 
ON INS.ANIMAL_ID = OUTS.ANIMAL_ID
WHERE INS.ANIMAL_ID IS NULL 
ORDER BY INS.ANIMAL_ID ASC;


